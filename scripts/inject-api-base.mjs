/**
 * Writes frontend/static/js/api-config.js from TRACTOR_API_BASE (Vercel / CI).
 * Example: TRACTOR_API_BASE=https://your-api.onrender.com
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const out = path.join(root, "frontend/static/js/api-config.js");
const base = (process.env.TRACTOR_API_BASE || "").trim().replace(/\/$/, "");
const content = `/* Generated by scripts/inject-api-base.mjs — do not edit by hand */\nwindow.__TRACTOR_API_BASE__ = ${JSON.stringify(base)};\n`;
fs.writeFileSync(out, content);
console.log("api-config.js:", base ? `API base set (${base.slice(0, 40)}…)` : "empty (set TRACTOR_API_BASE for production UI)");
